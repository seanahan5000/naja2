                PictMoveTo 121;23
                PictLineTo 121;51
                PictLineTo 259;51
                PictLineTo 259;23

                PictSetBlack1
                PictMoveTo 241;98       ; vertical wall line
                PictLineTo 241;149

                PictMoveTo 4;30         ; arrow bottom
                PictLineTo 37;30
                PictMoveTo 2;31
                PictLineTo 37;31

                PictSetWhite2
                PictMoveTo 168;99       ; clean up color artifacts
                PictLineTo 168;105
                PictMoveTo 168;139
                PictLineTo 168;148

                PictShowPage
                PictEnd

                SEC                     ;***
                RTS                     ;***

                LDA #$7F
                PHA
                LDA #$FF
                PHA
                LDA #fileInfirmary_Control
                LDY #$80
                JMP LOAD_FILE

